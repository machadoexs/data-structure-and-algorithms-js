console.log("for")
for (let i = 0; i < 10; i++) {
  console.log(i)
}

console.log("=================================================")
console.log("while")
var i = 0
while(i < 10) {
  console.log(i)
  i++
}

console.log("=================================================")
console.log("do while")
var i = 0
do {
  console.log(i)
  i++
} while(i < 10)
